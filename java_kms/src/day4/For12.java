package day4;



public class For12 {
	
	public static void main(String[] args) {
		/*
		 * 하나의 *를 이용하여 다음과 같이 출력
		 * *****
		 * *****
		 * *****
		 * *****
		 * *****
		 * */
		
		for(int j=1; j <=5 ; j++) {
			for(int i = 1; i<=5; i++) {
				
				System.out.print("*");
			}	
			System.out.println();
		}
		
		
		
		
	}

}
